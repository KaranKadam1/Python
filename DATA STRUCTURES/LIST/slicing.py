
data = [56,12,4,78,19,23,29,10,36,44,99]

print(data[0:3])  #[56, 12, 4]
print(data[5:9])  #[23, 29, 10, 36]
print(data[-1])   #99
print(data[0:-1]) #[56, 12, 4, 78, 19, 23, 29, 10, 36, 44]
print(data[-5:-1])#[29, 10, 36, 44]
print(data[5:-1]) #[23, 29, 10, 36, 44]
print(data[5:])   #[23, 29, 10, 36, 44, 99]
print(data[-3:])  #[36, 44, 99]
print(data[:])    #[56, 12, 4, 78, 19, 23, 29, 10, 36, 44, 99]


print(data[::2])  #[56, 4, 19, 29, 36, 99]
print(data[::3])  #[56, 78, 29, 44]
print(data[::-1]) #[99, 44, 36, 10, 29, 23, 19, 78, 4, 12, 56] reverse